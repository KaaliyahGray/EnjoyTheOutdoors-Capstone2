 // Get the dropdown element and display the info
 const dropdown = document.getElementById('mountainDropdown');
 const infoDisplay = document.getElementById("mountainInfo");

 // getting the dropdown options
mountains.forEach(function(mountain) {
    let option = document.createElement("option");
    option.text = mountain.Mountname;
    option.value = mountain.Mountname; // You can use a unique identifier here if needed
    dropdown.add(option);
});
// Add event listener to the dropdown to detect changes
dropdown.addEventListener("change", function() {
    // Retrieve the selected option
    const selectedMountainName = dropdown.value;

    // Find the selected mountain object from the mountains array
    const selectedMountain = mountains.find(mountain => mountain.Mountname === selectedMountainName);

    // Display information based on the selected mountain object
    if (selectedMountain) {
        infoDisplay.innerHTML = `
            <h2>${selectedMountain.Mountname}</h2>
            <p><strong>Description:</strong> ${selectedMountain.description}</p>
            <p><strong>Elevation:</strong> ${selectedMountain.elevation}</p>
            <p><strong>Location:</strong> ${selectedMountain.coords}</p>
            <p><strong>Level of Difficulty:</strong> ${selectedMountain.levelofdifficulty}</p>
            <img src="${selectedMountain.imageurl}" alt="${selectedMountain.Mountname}">
        `;
    } else {
        infoDisplay.textContent = ""; // Clear the information display if no option is selected
    }
});


